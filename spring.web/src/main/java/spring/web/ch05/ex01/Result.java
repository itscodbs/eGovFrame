package spring.web.ch05.ex01;

public class Result {
	private boolean duplicated;
	private String avaliableId;
	
	public boolean isDuplicated() {
		return duplicated;
	}
	public void setDuplicated(boolean duplicated) {
		this.duplicated = duplicated;
	}
	public String getAvaliableId() {
		return avaliableId;
	}
	public void setAvaliableId(String avaliableId) {
		this.avaliableId = avaliableId;
	}	
}
